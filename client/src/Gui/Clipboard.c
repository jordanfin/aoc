////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <Gui/Clipboard.h>
#include <stdlib.h>
#include <string.h>

////////////////////////////////////////////////////////////
// Member data
////////////////////////////////////////////////////////////
char ClipboardText[GUI_CLIPBOARD_SIZE];

////////////////////////////////////////////////////////////
/// Get the clipboard text
////////////////////////////////////////////////////////////
char * GuiClipboardGet()
{
	return ClipboardText;
}

////////////////////////////////////////////////////////////
/// Set the clipboard text
////////////////////////////////////////////////////////////
void GuiClipboardSet(char * Text, UInt32 Size)
{
	if (Text)
	{
		if (strlen(Text) < GUI_CLIPBOARD_SIZE)
		{
			memcpy(ClipboardText, Text, Size);

			ClipboardText[Size] = NULLCHAR;
		}
	}
}