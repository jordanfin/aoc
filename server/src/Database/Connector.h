#ifndef DATABASE_CONNECTOR_H
#define DATABASE_CONNECTOR_H

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <Config.h>
#include <System/Error.h>	///< Error handler
#include <my_global.h>		///< MySQL includes
#include <mysql.h>

///////////////////////////////////////////////////////////////////////////
///
/// Module for managing and securing data stored in the database
///
/// ======================================
/// MySQL Server Configuration (default)
/// Server:    localhost
/// UserName:  root
/// Password:  root
/// DataBase:  aocdb
/// Port:      3306
/// ======================================
///
///////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////
// Member data
////////////////////////////////////////////////////////////
struct DataBase
{
	MYSQL * Connection;
	char	Server[0xFF];
	char	User[0xFF];
	char	Password[0xFF];
	char	Database[0xFF];
	UInt32	Port;
};

////////////////////////////////////////////////////////////
// Defines
////////////////////////////////////////////////////////////
#define DATABASE_SUCCESS	true
#define DATABASE_ERROR		false

////////////////////////////////////////////////////////////
/// Create a connector
////////////////////////////////////////////////////////////
struct DataBase * ConnectorCreate(char * Server, char * User, char * Password, char * Database, UInt32 Port);

////////////////////////////////////////////////////////////
/// Connect from MySQL database
////////////////////////////////////////////////////////////
bool ConnectorInitialize(struct DataBase * DB);

////////////////////////////////////////////////////////////
/// Disconnect from MySQL database
////////////////////////////////////////////////////////////
bool ConnectorDestroy(struct DataBase * DB);

#endif // DATABASE_CONNECTOR_H